"""
Tipos de dados

* String - Textos
* Int - Inteiros (números inteiro, seja negativo ou positivo)
* float - real/ponto fluante, números que tenha "."
* bool - booleano/lógico (true ou false), geralmente vistos 
em comparações.

"""
#String: nome
print ('Gislane', type ('Gislane'))

#Idade: int
print (34, type (34))

#Altura: float
print ('1.70', type (1.70))

#Idade: bool
print (34 > 18, type (34 > 18))