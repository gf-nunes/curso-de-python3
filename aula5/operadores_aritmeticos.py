"""
Operadores Aritméticos
+, -, *, /, // (resultado da divisão inteira - arredondando), ** (exponenciação, 
número elevado a 1), % (retorna um módulo, o resto de uma divisão), () (alterar
a precedência das contas).

"""

print (10*10) #Multiplicação *
print (10+10) #Adição +
print (10-5) #Subtração -
print (10/2) #Divisão /
 
print (10*'10') #O operador de multiplicação também serve como operador de
#repetição. Se eu multiplico um inteiro por uma string eu tenho uma repetição.

# O operador de adição também faz uma concatenação entre duas strings 

print(10 // 3) #a divisão inteira retorna um valor flutuante

print (2 ** 10)

print (10 % 3)



